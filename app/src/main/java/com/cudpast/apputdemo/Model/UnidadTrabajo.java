package com.cudpast.apputdemo.Model;

public class UnidadTrabajo {
    private String idUT;
    private String nameUT;
    private String user_ui;
    private String aliasUT;

    public UnidadTrabajo() {
    }

    public String getIdUT() {
        return idUT;
    }

    public void setIdUT(String idUT) {
        this.idUT = idUT;
    }

    public String getNameUT() {
        return nameUT;
    }

    public void setNameUT(String nameUT) {
        this.nameUT = nameUT;
    }

    public String getUser_ui() {
        return user_ui;
    }

    public void setUser_ui(String user_ui) {
        this.user_ui = user_ui;
    }


    public String getAliasUT() {
        return aliasUT;
    }

    public void setAliasUT(String aliasUT) {
        this.aliasUT = aliasUT;
    }
}
